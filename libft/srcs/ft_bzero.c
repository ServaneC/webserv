#include <stddef.h>

void	ft_bzero(void *s, int n)
{
	int		i;
	char	*c;

	i = 0;
	if (n == 0)
		return ;
	c = s;
	while (i < n)
	{
		c[i] = 0;
		i++;
	}
	return ;
}
